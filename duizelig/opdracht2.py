# Print de uren van de dag. Voor de ochtend voeg je 'AM' toe. Voor de middag voeg je 'PM' toe
i = 1
while i <= 12:
        print(i, "am")
        i = i + 1
        if i == 0:
            break
        
j = 1
while j <= 12:
        print(j, "pm")
        j = j + 1
        if j == 0:
            break